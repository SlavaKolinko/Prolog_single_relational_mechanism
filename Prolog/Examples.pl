% =========================================================
% 1) Представлення множин: відношення 
% =========================================================

% Факти, встановлюють відношення між елементами
% Відношення студенту з одним атрибутом (Student)
student(alice).
student(bob).

% Відношення бере курс з двома атрибутами (Student, Course)
takes(alice, db).
takes(alice, prolog).
takes(bob, prolog).

% Відношення викладає курс з двома атрибутами (Course, Teacher)
teaches(db, anna).
teaches(prolog, oleksii).

% Мультипризначеність:
% (--, --, --) - згенерувати всі можливі кортежі студентів, курсів та викладачів, які задовольняють умови правил
% (+ , -- , --) - перевірити, чи студент відвідує певний курс, і хто його викладає
% (+ , + , --) - перевірити, чи студент відвідує курс, який викладається певним викладачем

%2) Відсутність інших змістовних призначень:
% Генерація всіх можливих варіантів за принципом (студент, курс, викладач) не має сенсу для задачі, 
% оскільки є обмеження лише існуючими фактами, визначеними заздалегідь

% Правило: студент відвідує курс викладача
attends_with_teacher(S, C, T) :-
    student(S),
    takes(S, C),
    teaches(C, T).

/** <examples>
?- attends_with_teacher(S, C, T).
% S = alice, C = db,     T = anna ;
% S = alice, C = prolog, T = oleksii ;
% S = bob,   C = prolog, T = oleksii ;
% false.

?- attends_with_teacher(alice, C, T).
% C = db,     T = anna ;
% C = prolog, T = oleksii ;
% false.
*/


% =========================================================
% 2) Декартів добуток: узагальнений випадок 
% =========================================================

% Факти
r(1).
r(2).

s(a).
s(b).

t(x).
t(y).

% Мультипризначеність:
% (-- , -- , --) - згенерувати всі можливі кортежі для X, Y, Z, що задовольняють умови кожного з предикатів
% (+ , -- , --) - перевірити, чи існує певне значення для X, Y, Z, яке задовольняє умови трьох предикатів
% (+ , + , --) - перевірити, чи є певне значення для X, Y та відповідне значення для Z

% Правило: декартів добуток
p(X, Y, Z) :-
    r(X),
    s(Y),
    t(Z).

/** <examples>
?- p(X, Y, Z).
X = 1, Y = a, Z = x ;
X = 1, Y = a, Z = y ;
X = 1, Y = b, Z = x ;
X = 1, Y = b, Z = y ;
X = 2, Y = a, Z = x ;
X = 2, Y = a, Z = y ;
X = 2, Y = b, Z = x ;
X = 2, Y = b, Z = y ;
false.
*/


% =========================================================
% 3) θ-обмеження у Prolog 
% =========================================================

% Факти
r1(a, 1).
r1(b, 2).
r1(c, 3).

r2(a, 1).
r2(b, 2).
r2(d, 4).

% Мультипризначеність:
% (-- , --) - згенерувати всі можливі кортежі (X, Y) для r1(X, Y) та r2(X, Y), де кожен кортеж одночасно належить обом відношенням
% (+ , --) - перевірити, чи існує певне значення (X, Y), яке одночасно задовольняє умови для обох предикатів
% (+ , +) — перевірити, чи є кортеж (X, Y), що належить обом відношенням

% Правило: перетин
intersection(X, Y) :-
    r1(X, Y),
    r2(X, Y).

/** <examples>
?- intersection(X, Y).
X = a, Y = 1 ;
X = b, Y = 2 ;
false.
*/


% =========================================================
% 4) Природне θ-з’єднання та його узагальнення   
% =========================================================

% Факти
r(1, a).
r(2, b).
r(3, c).

s(a, x).
s(b, y).
s(c, z).

% Мультипризначеність:
% (-- , -- , --) — згенерувати всі можливі кортежі (X, Y, Z) для r(X, Y) та s(Y, Z), що задовольняють умову з’єднання через спільну змінну Y
% (+ , -- , --) — перевірити, чи існує певне значення для X і Y, яке задовольняє умову з’єднання з s(Y, Z)
% (+ , + , --) — перевірити, чи є кортеж (X, Y, Z), який задовольняє умови для обох реляцій

% Природне з’єднання: поєднуємо r/2 та s/2 на основі спільної змінної Y
join(X, Y, Z) :-
    r(X, Y),
    s(Y, Z).

/** <examples>
?- join(X, Y, Z).
X = 1, Y = a, Z = x ;
X = 2, Y = b, Z = y ;
X = 3, Y = c, Z = z ;
false.    
*/


% 4.1 Природне з’єднання для кількох реляцій

% Факти
r1(1, a).
r1(2, b).
r1(3, c).

r2(a, x).
r2(b, y).
r2(c, z).

r3(x, p).
r3(y, q).
r3(z, r).

% Природне з’єднання для трьох реляцій
join(A, B, C, D) :-
    r1(A, B),
    r2(B, C),
    r3(C, D).


/** <examples>
?- join(A, B, C, D).
A = 1, B = a, C = x, D = p ;
A = 2, B = b, C = y, D = q ;
A = 3, B = c, C = z, D = r ;
false.  
*/


% 5) Узагальнений випадок 

% Факти
r1(1, a).
r1(2, b).
r1(3, c).

r2(a, x).
r2(b, y).
r2(c, z).

r3(x, p).
r3(y, q).
r3(z, r).

r4(1, p).
r4(2, q).
r4(3, r).

r5(t).
r5(u).

% Мультипризначеність:
% (-- , -- , -- , -- , --) — згенерувати всі можливі кортежі для A, B, C, D, E, що задовольняють умови з’єднання та перетину
% (+ , -- , -- , -- , --) — перевірити, чи існує певне значення для A, B, C, D, E, що задовольняє всі умови з’єднання та перетину
% (+ , + , + , -- , --) — перевірити, чи є кортеж, що задовольняє умови для всіх реляцій

% Узагальнене з’єднання з кількома предикатами
q(A, B, C, D, E) :-
    r1(A, B),
    r2(B, C),
    r3(C, D),
    r4(A, D),
    r5(E). 

/** <examples>
?- q(A, B, C, D, E).
A = 1, B = a, C = x, D = p, E = t
A = 1, B = a, C = x, D = p, E = u
A = 2, B = b, C = y, D = q, E = t
A = 2, B = b, C = y, D = q, E = u
A = 3, B = c, C = z, D = r, E = t
A = 3, B = c, C = z, D = r, E = u
false.
*/

